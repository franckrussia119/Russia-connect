export type Language = "en" | "fr" | "ru";
export type Translations = typeof translations.en;

// Elegant currency formatter based on chosen language
// 1 Euro = 650 FCFA
// 1 Euro = 100 RUB
export function formatAmount(euroValue: number, lang: Language): string {
  if (lang === "fr") {
    const fcfa = Math.round(euroValue * 650);
    return fcfa.toLocaleString("fr-FR") + " FCFA";
  } else if (lang === "ru") {
    const rub = Math.round(euroValue * 100);
    return rub.toLocaleString("ru-RU") + " ₽";
  } else {
    // English defaults to displaying both for complete convenience
    const fcfa = Math.round(euroValue * 650);
    const rub = Math.round(euroValue * 100);
    return `${fcfa.toLocaleString("en-US")} FCFA (${rub.toLocaleString("en-US")} ₽)`;
  }
}
